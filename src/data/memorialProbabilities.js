// ============================================================
// 헬퍼: 한 메모리얼의 고유 옵션 라벨 목록 (UI 셀렉트용)
// 같은 라벨은 중복 제거
// ============================================================
export function uniqueLabels(memorial) {
  const set = new Set();
  for (const tier of memorial.tiers) {
    set.add(tier[3]);
  }
  return Array.from(set);
}

// ============================================================
// 베이스 라벨 = 티어 prefix 제거한 옵션 이름
//   "[2] 근력/마법력" → "근력/마법력"
//   "고정 대미지%"     → "고정 대미지%"  (prefix 없음 — 그대로)
// ============================================================
export function baseLabelOf(fullLabel) {
  return fullLabel.replace(/^\[\d+\]\s*/, '');
}

export const ALLSTAT_BASE = '올스탯';

// ============================================================
// 한 메모리얼의 고유 베이스 옵션 목록 (티어 통합)
// ============================================================
export function uniqueBaseLabels(memorial) {
  const set = new Set();
  for (const tier of memorial.tiers) {
    set.add(baseLabelOf(tier[3]));
  }
  return Array.from(set);
}

// ============================================================
// 한 줄(label)이 목표(targetBase)에 기여하는지 판정
//   - 같은 베이스 라벨이면 기여
//   - 라인이 올스탯이고 목표가 올스탯이 아닌 경우 기여
//     (게임 내 올스탯 = 모든 스탯 동시 상승)
// ============================================================
export function lineContributesTo(lineLabel, targetBase) {
  const lineBase = baseLabelOf(lineLabel);
  if (lineBase === targetBase) return true;
  if (lineBase === ALLSTAT_BASE && targetBase !== ALLSTAT_BASE) return true;
  return false;
}
