/**
 * 라테일 메모리얼 raw tier 데이터
 *
 * 메커니즘 요약:
 *   - 한 번 굴림 = 1개 메모리얼 카드
 *   - 카드는 1~4줄 (line/slot)을 가짐. 줄 수는 Qdist로 결정
 *   - 각 줄마다 tier 풀에서 weight 비율로 하나 선택 (정규화 적용)
 *   - 선택된 tier의 [lo, hi] 범위 안에서 균등 분포로 값 추첨
 *
 * 일반(Normal) 메모리얼: Qdist 없이 줄 수 항상 1
 * 세트(Set) 메모리얼:    Qdist = {1:0.30, 2:0.30, 3:0.30, 4:0.10} (모든 메모리얼 공통)
 *
 * tier 형식: [lo, hi, weight, label]
 *   weight는 절대 확률이 아닌 상대 가중치 — 모든 tier weight 합으로 정규화됨
 */

